books_filename = 'BX-Books.csv'
ratings_filename = 'BX-Book-Ratings.csv'

# import csv data into dataframes
df_books = pd.read_csv(
    books_filename,
    encoding = "ISO-8859-1",
    sep=";",
    header=0,
    names=['isbn', 'title', 'author'],
    usecols=['isbn', 'title', 'author'],
    dtype={'isbn': 'str', 'title': 'str', 'author': 'str'})

df_ratings = pd.read_csv(
    ratings_filename,
    encoding = "ISO-8859-1",
    sep=";",
    header=0,
    names=['user', 'isbn', 'rating'],
    usecols=['user', 'isbn', 'rating'],
    dtype={'user': 'int32', 'isbn': 'str', 'rating': 'float32'})

df = df_ratings

#users with at least 200 ratings
user_counts = df['user'].value_counts()
active_users = user_counts[user_counts >= 200].index

#books with at least 100 ratings
book_counts = df['isbn'].value_counts()
popular_books = book_counts[book_counts >= 100].index

#remove
df = df[df['user'].isin(active_users)]
df = df[df['isbn'].isin(popular_books)]

df = pd.merge(right=df, left = df_books, on="isbn") # merge two tables

df = df.drop_duplicates(["title", "user"]) # consider only one rated by a person

piv = df.pivot(index='title', columns='user', values='rating').fillna(0) # prep for cosine similarity

matrix=piv.values
matrix.shape

model_knn=NearestNeighbors(metric='cosine',algorithm='brute')
model_knn.fit(matrix)

# function to return recommended books - this will be tested
def get_recommends(book = ""):
  x=piv.loc[book].array.reshape(1, -1)
  distances,indices=model_knn.kneighbors(x,n_neighbors=6)
  R_books=[]
  for distance,indice in zip(distances[0],indices[0]):
    if distance!=0:
      R_book=piv.index[indice]
      R_books.append([R_book,distance])
  recommended_books=[book,R_books[::-1]]
  return recommended_books

books = get_recommends("Where the Heart Is (Oprah's Book Club (Paperback))")
print(books)

